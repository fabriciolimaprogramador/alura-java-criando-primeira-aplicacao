public class Temperatura {


  static void main(String[] args) {

    System.out.println("Converte temperatura de Celsius para Fahrenheit");

    double celsius = 55.44;

    double fahrenheit = (celsius * 1.8) + 32;

    System.out.println("Fahrenheit: " + fahrenheit);
    System.out.println("Fahrenheit inteiro: " + (int) fahrenheit);

  }

}
